import type { Patient, VitalHistory } from '@/types';

function generateHistory(base: {
  hr: number; temp: number; sbp: number; dbp: number;
}, count = 24): VitalHistory[] {
  return Array.from({ length: count }, (_, i) => {
    const hoursAgo = count - i;
    const d = new Date(Date.now() - hoursAgo * 60 * 60 * 1000);
    return {
      time: d.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' }),
      heartRate: base.hr + Math.round((Math.random() - 0.5) * 14),
      temperature: parseFloat((base.temp + (Math.random() - 0.5) * 0.6).toFixed(1)),
      systolicBP: base.sbp + Math.round((Math.random() - 0.5) * 16),
      diastolicBP: base.dbp + Math.round((Math.random() - 0.5) * 10),
    };
  });
}

