export const getPatientById = (id: string): Patient | undefined =>
  patients.find((p) => p.id === id);

export const getPatientsByStatus = (status: Patient['status']): Patient[] =>
  patients.filter((p) => p.status === status);

export const getDashboardStats = () => ({
  totalPatients: patients.length,
  criticalPatients: patients.filter((p) => p.status === 'critical').length,
  warningPatients: patients.filter((p) => p.status === 'warning').length,
  stablePatients: patients.filter((p) => p.status === 'stable').length,
  activeAlerts: patients.filter((p) => p.status === 'critical' || p.status === 'warning').length,
  devicesOnline: patients.length - 1,
});
